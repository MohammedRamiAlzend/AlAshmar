export function useT(): typeof translations.en {
  const lang = useSettingsStore(s => s.lang);
  return translations[lang] as typeof translations.en;
}

export function useDir(): 'ltr' | 'rtl' {
  const lang = useSettingsStore(s => s.lang);
  return lang === 'ar' ? 'rtl' : 'ltr';
}
